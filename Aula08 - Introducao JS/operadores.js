let numero1 = 5
let numero2 = 5

// Operador soma

let soma = numero1 + numero2
console.log("A soma foi igual a " + soma)

// Operador subtração

let subtracao = numero1 - numero2
console.log("A subtração foi igual a " + subtracao)

// Operador multiplicação

let multiplicacao  = numero1 * numero2
console.log("A multiplicação foi igual a " + multiplicacao)

// Operador divisao

let divisao = numero1 / numero2
console.log("A divisao foi igual a " + divisao)

// Operador modulo

numero2 = 2
let modulo = numero1 % numero2
console.log("O modulo foi igual a " + modulo)

// Potência - Math.pow()

let base = 5
let expoente = 2
let potencia = Math.pow(base, expoente)
console.log("A potencia é igual a " + potencia)

// Radiciação - Math.sqrt()

let radiciacao = Math.sqrt(potencia)
console.log("A soma foi igual a " + radiciacao)